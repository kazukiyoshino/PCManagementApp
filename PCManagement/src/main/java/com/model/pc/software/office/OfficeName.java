package com.model.pc.software.office;

public class OfficeName {
	private String name;

	public OfficeName() {

	}

	public OfficeName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "OfficeName [name=" + name + "]";
	}




}
